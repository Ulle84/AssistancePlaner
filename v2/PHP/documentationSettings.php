<h1>Einstellungen</h1>
Über die Einstellungen kann man den Assistenzplaner konfigurieren. <br />

<h2>Vorname</h2>
Hier wird der Vorname des Klienten eingetragen.

<h2>Nachname</h2>
Hier wird der Vorname des Klienten eingetragen.

<h2>E-Mail Adresse</h2>
Hier wird die E-Mail Adresse des Klienten eingetragen. Dies muss nicht zwangsläufig erfolgen. Der Assistenzplaner funktioniert
auch ohne die E-Mail Adresse des Klienten.

<h2>Standard-Passwort für Assistenten</h2>
Wenn ein Assistent neu angelegt wird oder das Passwort zurückgesetzt wird, dann hat bekommt er das hier hinterlegte
Passwort zugewiesen.

<h2>Aufgaben-Verwaltung nutzen</h2>
Hier kann man einstellen ob man die Aufgaben-Verwaltung nutzen möchte oder nicht.