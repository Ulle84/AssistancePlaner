<h1>Navigation</h1>
Ganz oben rechts ist immer die Navigation zu sehen.
<img id="imgNavigation" title="Navigation" class="highResolution" src="../Images/Screenshots/NavigationLoggedIn.png"/>
Der Inhalt verändert sich, wenn man sich am System an- bzw. abmeldet.

<h2>Klient</h2>
Hier kann man den Namen des Klienten sehen.

<h2>Assistent</h2>
Hier kann man den aktuell am System angemeldeten Assistenten sehen. Will man mit einem anderen Assistenten-Account arbeiten,
so kann man auf den Link "Abmelden" klicken und sich mit einem anderen Assistenten-Account am System anmelden.

<h2>Übersicht</h2>
Klickt man auf den Link Übersicht, so gelangt man zur Übersicht. Von dort aus kann man alle Seiten des Assistenzplaners
erreichen.

<h2>Passwort ändern</h2>
Möchte man sein Passwort ändern, so kann man das über den Link "Passwort ändern" tun. Man gelingt auf eine Seite, wo man
das aktuelle Passwort eingeben muss. Zusätzlich muss man ein neues Passwort und die Wiederholung dessen eingeben. Um
die Aktion abzuschließen drückt man auf den Button <input type="button" value="Passwort ändern"/>.

<h2>Dokumentation</h2>
Wenn man auf den Link "Dokumentation" klickt, dann gelangt man auf diese Dokumentations-Seite.

<h2>Feedback</h2>
Hier gelangt man zu einer Seite, auf der man sein Feedback formulieren und an den Entwickler des Assistenzplaners schicken kann.

<h2>Impressum</h2>
Hier gelangt man zu Impressum, Haftungsausschluss und Datenschutzerklärung.

<h2>Abmelden</h2>
Wenn man seine Arbeiten abgeschlossen hat und den Assistenzplaner verlassen möchte, so sollte man auf den Link
"Abmelden" klicken. Man wird vom System abgemeldet und landet auf der Seite, wo man sich erneut am System anmelden kann.